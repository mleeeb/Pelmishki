/**
 * @file main.cpp
 * @brief Точка входа в игру-кликер на SDL3.
 *
 * Этот файл содержит главную функцию main(), которая инициализирует
 * игровой объект, запускает главный игровой цикл и управляет его выполнением.
 * 
 * Игра представляет собой кликер, где игрок должен кликать по врагам,
 * чтобы победить их. Все враги занимают весь экран, а урон наносится
 * кликом в любом месте окна.
 */

#include <SDL3/SDL_main.h>
#include "Game.h"

/**
 * @brief Глобальный объект игры.
 * 
 * Создаётся один раз при запуске программы и существует на протяжении
 * всего времени выполнения. Содержит все игровые данные и логику.
 */
Game game;

/**
 * @brief Главная функция - точка входа в программу.
 * 
 * Выполняет следующие задачи:
 * 1. Инициализирует игру через game.SDL_AppInit()
 * 2. Запускает главный игровой цикл:
 *    - Обрабатывает все ожидающие события
 *    - Обновляет и отрисовывает игровой кадр
 *    - Добавляет задержку для ограничения FPS (~60 кадров в секунду)
 * 3. При завершении цикла освобождает ресурсы через game.SDL_AppQuit()
 * 
 * Главный цикл продолжается до тех пор, пока не будет получен сигнал
 * завершения (SDL_APP_SUCCESS от SDL_AppEvent или SDL_AppIterate).
 * 
 * @param argc Количество аргументов командной строки (не используется).
 * @param argv Массив аргументов командной строки (не используется).
 * @return 0 при успешном завершении, -1 при ошибке инициализации.
 * 
 * @note Функция оформлена в соответствии с требованиями SDL3,
 *       где main должна иметь именно такую сигнатуру.
 */
int main(int argc, char* argv[]) {
    // Инициализация игры
    if (game.SDL_AppInit() != SDL_APP_CONTINUE) {
        return -1;  // Критическая ошибка инициализации (обычно шрифт не загружен)
    }

    bool running = true;
    SDL_Event event;

    // Главный игровой цикл
    while (running) {
        // Обработка всех событий в очереди
        while (SDL_PollEvent(&event)) {
            if (game.SDL_AppEvent(&event) == SDL_APP_SUCCESS) {
                running = false;  // Получен сигнал выхода из игры
                break;
            }
        }

        // Обновление и отрисовка кадра
        if (game.SDL_AppIterate() == SDL_APP_SUCCESS) {
            running = false;  // Игровой цикл завершён
        }

        // Задержка для ограничения FPS (примерно 60 кадров в секунду)
        // 1000 мс / 60 ≈ 16.67 мс, округляем до 16 мс
        SDL_Delay(16);
    }

    // Освобождение ресурсов перед выходом
    game.SDL_AppQuit(SDL_APP_CONTINUE);
    return 0;
}
