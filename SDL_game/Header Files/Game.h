/**
 * @file Game.h
 * @brief Заголовочный файл класса Game, управляющего основным игровым процессом.
 *
 * Этот файл содержит объявление класса Game, который отвечает за инициализацию,
 * основной цикл, обработку событий, отрисовку и управление врагами в игре-кликере.
 */

#pragma once
#include <SDL3/SDL.h>
#include <SDL3_image/SDL_image.h>
#include <SDL3_ttf/SDL_ttf.h>
#include <vector>

#include "Enemy.h"

/**
 * @class Game
 * @brief Главный игровой класс, управляющий всем приложением.
 *
 * Класс Game реализует паттерн "одиночка" в контексте приложения SDL.
 * Он отвечает за создание окна, рендерера, загрузку шрифтов, управление
 * списком врагов, обработку кликов мыши и отображение экрана победы.
 */
class Game
{
public:
    /**
     * @brief Конструктор класса Game.
     *
     * Инициализирует все внутренние переменные состояния игры значениями по умолчанию.
     * Текстурa фона, текстура победы и список врагов пока не создаются.
     */
    Game();

    /**
     * @brief Деструктор класса Game.
     *
     * Освобождает всю выделенную память: удаляет всех врагов из списка,
     * уничтожает текстуры фона и победы.
     */
    ~Game();

    /**
     * @brief Инициализирует все подсистемы SDL и загружает ресурсы.
     *
     * Создаёт окно и рендерер, инициализирует SDL, SDL_image и SDL_ttf.
     * Пытается загрузить шрифт из нескольких возможных путей.
     * Загружает текстурный фон и создаёт всех врагов.
     *
     * @return SDL_APP_CONTINUE при успешной инициализации,
     *         SDL_APP_FAILURE при критической ошибке (например, шрифт не загружен).
     */
    SDL_AppResult SDL_AppInit();

    /**
     * @brief Обрабатывает входящие события SDL.
     *
     * Реагирует на событие выхода из приложения и на клики левой кнопкой мыши.
     * При клике во время активной игры наносит урон текущему врагу.
     * При клике на экране победы перезапускает игру.
     *
     * @param event Указатель на событие SDL.
     * @return SDL_APP_SUCCESS если получен сигнал завершения,
     *         SDL_APP_CONTINUE в остальных случаях.
     */
    SDL_AppResult SDL_AppEvent(SDL_Event* event);

    /**
     * @brief Выполняет один кадр игрового цикла.
     *
     * Очищает экран, отрисовывает фон, всех врагов и,
     * при необходимости, экран победы с оверлеем.
     * После отрисовки вызывает SDL_RenderPresent.
     *
     * @return Всегда возвращает SDL_APP_CONTINUE.
     */
    SDL_AppResult SDL_AppIterate();

    /**
     * @brief Освобождает все ресурсы перед выходом из приложения.
     *
     * Уничтожает окно, рендерер, закрывает шрифт,
     * завершает работу подсистем SDL_ttf и SDL.
     *
     * @param result Результат работы приложения (не используется напрямую).
     */
    void SDL_AppQuit(SDL_AppResult result);

    /**
     * @brief Устанавливает указатели на окно и рендерер извне.
     *
     * Этот метод не используется в текущей реализации, так как
     * окно и рендерер создаются внутри SDL_AppInit.
     *
     * @param win Указатель на SDL_Window.
     * @param ren Указатель на SDL_Renderer.
     */
    void SetWindowAndRenderer(SDL_Window* win, SDL_Renderer* ren) {
        window = win;
        renderer = ren;
    }

    SDL_FRect mainrect;                 ///< Прямоугольник для отрисовки фона на весь экран.

    static constexpr int SCREEN_WIDTH = 540;   ///< Ширина окна игры в пикселях.
    static constexpr int SCREEN_HEIGHT = 720;  ///< Высота окна игры в пикселях.

private:
    /**
     * @brief Создаёт всех врагов для текущей игры.
     *
     * Очищает предыдущий список врагов, затем создаёт трёх врагов
     * с возрастающим уровнем и здоровьем. Пытается загрузить текстуры
     * для каждого врага из папки assets. Активирует первого врага.
     */
    void spawnEnemies();

    /**
     * @brief Проверяет условия победы.
     *
     * Если все враги побеждены и игра ещё активна,
     * устанавливает флаги победы и отображает экран победы.
     */
    void checkVictory();

    /**
     * @brief Создаёт текстурный экран победы.
     *
     * Рендерит текст "VICTORY! Click to Play Again" золотым цветом
     * и сохраняет текстуру с вычисленной позицией по центру экрана.
     */
    void showVictoryScreen();

    /**
     * @brief Перезапускает игру.
     *
     * Удаляет всех существующих врагов, очищает текстуру победы
     * и вызывает spawnEnemies() для создания новой игры.
     */
    void resetGame();

    /**
     * @brief Проверяет, находится ли точка внутри прямоугольника.
     *
     * Вспомогательный метод для проверки кликов по UI-элементам.
     *
     * @param x Координата X точки (обычно мыши).
     * @param y Координата Y точки.
     * @param rect Прямоугольник для проверки.
     * @return true если точка внутри прямоугольника, иначе false.
     */
    bool isPointInRect(float x, float y, SDL_FRect rect);

    /**
     * @brief Активирует следующего врага в очереди.
     *
     * Деактивирует текущего врага, переключает индекс на следующего,
     * активирует его. Если врагов больше нет, вызывает checkVictory().
     */
    void activateNextEnemy();

    /**
     * @brief Создаёт градиентный текстурный фон.
     *
     * Создаёт текстуру-мишень и заполняет её вертикальным градиентом
     * от тёмно-сине-зелёного к более светлому оттенку.
     */
    void loadBackground();

    SDL_Window* window;          ///< Указатель на окно SDL.
    SDL_Renderer* renderer;      ///< Указатель на рендерер SDL.
    TTF_Font* font;              ///< Указатель на загруженный шрифт TTF.

    std::vector<Enemy*> enemies; ///< Вектор указателей на всех врагов в игре.
    int currentEnemyIndex;       ///< Индекс текущего активного врага.
    bool gameActive;             ///< Флаг активности основного игрового процесса.
    bool victoryScreen;          ///< Флаг отображения экрана победы.

    SDL_Texture* victoryTexture; ///< Текстура с сообщением о победе.
    SDL_FRect victoryRect;       ///< Прямоугольник позиции текста победы.

    SDL_Texture* backgroundTexture; ///< Текстура градиентного фона.
};
