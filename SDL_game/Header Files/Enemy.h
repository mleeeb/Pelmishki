/**
 * @file Enemy.h
 * @brief Заголовочный файл класса Enemy, представляющего игрового врага.
 *
 * Этот файл содержит объявление класса Enemy, который отвечает за отображение,
 * поведение и обработку урона врага в игре-кликере. Враг может отображаться
 * как текстура (изображение) либо как цветной прямоугольник, если текстура не загружена.
 */

#pragma once
#include <SDL3/SDL.h>
#include <SDL3_image/SDL_image.h>
#include <SDL3_ttf/SDL_ttf.h>
#include <string>

/**
 * @class Enemy
 * @brief Класс, представляющий противника в игре.
 *
 * Класс Enemy управляет состоянием одного врага: его здоровьем, уровнем,
 * активностью, отображением (текстура или цветной прямоугольник),
 * полоской здоровья и текстовой информацией на экране.
 * 
 * Враг занимает весь экран (540x720 пикселей) и может быть активирован
 * или деактивирован. При получении урона уменьшается его здоровье,
 * обновляется текст и цвет полоски здоровья.
 */
class Enemy {
public:
    /**
     * @brief Конструктор класса Enemy.
     *
     * Инициализирует врага с заданными параметрами здоровья, уровня и позиции.
     * Пытается загрузить текстуру из указанного файла. Если текстура не загружена,
     * устанавливает цвет фона в зависимости от уровня врага.
     * Вызывает updateText() для создания текстового отображения здоровья.
     *
     * @param renderer Указатель на SDL_Renderer для отрисовки.
     * @param font Указатель на загруженный TTF_Font для текста.
     * @param health Начальное и максимальное здоровье врага.
     * @param level Уровень врага (влияет на цвет фона, если нет текстуры).
     * @param x Координата X позиции врага (обычно 0, на весь экран).
     * @param y Координата Y позиции врага (обычно 0, на весь экран).
     * @param imagePath Путь к файлу текстуры врага (может быть nullptr).
     */
    Enemy(SDL_Renderer* renderer, TTF_Font* font, int health, int level, int x, int y, const char* imagePath = nullptr);

    /**
     * @brief Деструктор класса Enemy.
     *
     * Уничтожает текстуры текста и текстуру врага, если они были созданы.
     */
    ~Enemy();

    /**
     * @brief Обновляет состояние врага.
     *
     * В текущей реализации метод зарезервирован для будущей анимации.
     * Вызывается каждый кадр игрового цикла.
     */
    void update();

    /**
     * @brief Отрисовывает врага на экране.
     *
     * Если враг не активен, отрисовка не выполняется.
     * При наличии текстуры врага отображает её, растянутую на весь экран.
     * Иначе заливает прямоугольник цветом bgColor.
     * Всегда отрисовывает жёлтую рамку вокруг экрана, текст уровня вверху,
     * полоску здоровья внизу и текущее значение здоровья текстом.
     */
    void draw();

    /**
     * @brief Наносит урон врагу.
     *
     * Уменьшает текущее здоровье на указанное количество.
     * Здоровье не может стать меньше 0.
     * Обновляет текстовое отображение здоровья.
     * 
     * @param damage Количество урона (положительное целое число).
     * 
     * @note Если враг не активен или уже мёртв, метод ничего не делает.
     */
    void takeDamage(int damage);

    /**
     * @brief Проверяет, жив ли враг.
     * 
     * @return true если текущее здоровье больше 0, иначе false.
     */
    bool isAlive() const { return currentHealth > 0; }

    /**
     * @brief Возвращает текущее здоровье врага.
     * 
     * @return Текущее количество здоровья.
     */
    int getHealth() const { return currentHealth; }

    /**
     * @brief Возвращает прямоугольник, занимаемый врагом на экране.
     * 
     * @return SDL_FRect с координатами и размерами врага.
     */
    SDL_FRect getRect() const { return rect; }

    /**
     * @brief Возвращает уровень врага.
     * 
     * @return Уровень врага (1, 2 или 3).
     */
    int getLevel() const { return level; }

    /**
     * @brief Устанавливает активность врага.
     * 
     * Активный враг отображается на экране и может получать урон.
     * 
     * @param active true для активации, false для деактивации.
     */
    void setActive(bool active) { isActive = active; }

    /**
     * @brief Проверяет, активен ли враг.
     * 
     * @return true если враг активен, иначе false.
     */
    bool getActive() const { return isActive; }

private:
    /**
     * @brief Обновляет текстовое отображение здоровья.
     *
     * Уничтожает старую текстуру текста, если она существовала.
     * Создаёт новую поверхность с текстом в формате "текущее/максимальное"
     * и преобразует её в текстуру. Вычисляет размеры текста для позиционирования.
     * 
     * @note Если создание текстуры не удаётся, в лог выводится ошибка.
     */
    void updateText();

    /**
     * @brief Загружает текстуру врага из файла изображения.
     *
     * Пытается загрузить изображение через IMG_LoadTexture.
     * При успешной загрузке масштабирует текстуру на весь экран (540x720).
     * 
     * @param imagePath Путь к файлу изображения.
     * 
     * @note В случае ошибки текстура остаётся равной nullptr,
     *       и враг будет отображаться как цветной прямоугольник.
     */
    void loadTexture(const char* imagePath);

    SDL_Renderer* renderer;     ///< Указатель на SDL_Renderer для отрисовки.
    TTF_Font* font;             ///< Указатель на шрифт для текста.

    int currentHealth;          ///< Текущее здоровье врага.
    int maxHealth;              ///< Максимальное здоровье врага.
    int level;                  ///< Уровень врага (определяет сложность и цвет фона).
    SDL_FRect rect;             ///< Прямоугольник врага (всегда на весь экран 540x720).
    bool isActive;              ///< Флаг активности: true - враг отображается и может получать урон.

    std::string healthText;     ///< Строка текста здоровья в формате "текущее/максимальное".
    SDL_Texture* texture;       ///< Текстура с текстом здоровья.
    SDL_Texture* enemyTexture;  ///< Текстура изображения врага (если загружена).
    SDL_FRect destText;         ///< Прямоугольник для позиционирования текста здоровья.
    SDL_Color textColor;        ///< Цвет текста (белый, не используется напрямую в текущей реализации).
    SDL_Color bgColor;          ///< Цвет фона для отрисовки, если нет текстуры (зависит от уровня).
};
